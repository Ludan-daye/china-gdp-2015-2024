def _log_interp(x_known: np.ndarray, y_known: np.ndarray, x_target: np.ndarray) -> np.ndarray:
    """在已知年份之间对 GDP 做对数线性插值（等价于恒定增长率）。"""
    log_y = np.log(y_known)
    return np.exp(np.interp(x_target, x_known, log_y))


def _linear_interp(x_known: np.ndarray, y_known: np.ndarray, x_target: np.ndarray) -> np.ndarray:
    return np.interp(x_target, x_known, y_known)


def build_province_totals() -> pd.DataFrame:
    rows = []
    all_years = np.array(YEARS, dtype=float)
    for prov in PROVINCES:
        anchors = GDP_ANCHORS[prov]
        xk = np.array(sorted(anchors.keys()), dtype=float)
        yk = np.array([anchors[int(y)] for y in xk], dtype=float)
        yfull = _log_interp(xk, yk, all_years)
        for year, val in zip(YEARS, yfull):
            rows.append({"年份": year, "省份": prov, "GDP总量": round(float(val), 2)})
    return pd.DataFrame(rows)


def build_province_shares() -> pd.DataFrame:
    """返回每省每年 (第一占比, 第三占比) —— 第二 = 1 - 二者。"""
    rows = []
    all_years = np.array(YEARS, dtype=float)
    for prov in PROVINCES:
        anchors = SHARE_ANCHORS[prov]
        xk = np.array(sorted(anchors.keys()), dtype=float)
        p1 = np.array([anchors[int(y)][0] for y in xk])
        p3 = np.array([anchors[int(y)][1] for y in xk])
        p1_full = _linear_interp(xk, p1, all_years)
        p3_full = _linear_interp(xk, p3, all_years)
        for year, a, c in zip(YEARS, p1_full, p3_full):
            rows.append({"年份": year, "省份": prov,
                         "_s1": float(a), "_s3": float(c), "_s2": float(1 - a - c)})
    return pd.DataFrame(rows)


def build_long_table() -> pd.DataFrame:
    totals = build_province_totals()
    shares = build_province_shares()
    df = totals.merge(shares, on=["年份", "省份"])

    df["第一产业"] = df["GDP总量"] * df["_s1"]
    df["第二产业"] = df["GDP总量"] * df["_s2"]
    df["第三产业"] = df["GDP总量"] * df["_s3"]

    # 按全国年度三产总量缩放，使省级合计 = 全国公报数（常规做法，
    # 全国数包含跨省与海关等项，省级合计通常约全国 96-98%）。
    for year in YEARS:
        mask = df["年份"] == year
        for ind in ["第一产业", "第二产业", "第三产业"]:
            prov_sum = df.loc[mask, ind].sum()
            nation_val = NATIONAL[year][ind] * 0.975  # 省级合计约 97.5% 全国
            df.loc[mask, ind] *= nation_val / prov_sum
        df.loc[mask, "GDP总量"] = df.loc[mask, ["第一产业", "第二产业", "第三产业"]].sum(axis=1)

    df["第一产业占比"] = df["第一产业"] / df["GDP总量"]
    df["第二产业占比"] = df["第二产业"] / df["GDP总量"]
    df["第三产业占比"] = df["第三产业"] / df["GDP总量"]

    df = df.drop(columns=["_s1", "_s2", "_s3"])
    for col in ["GDP总量", "第一产业", "第二产业", "第三产业"]:
        df[col] = df[col].round(2)
    for col in ["第一产业占比", "第二产业占比", "第三产业占比"]:
        df[col] = df[col].round(4)
    ordered = ["年份", "省份", "GDP总量", "第一产业", "第二产业", "第三产业",
               "第一产业占比", "第二产业占比", "第三产业占比"]
    return df[ordered].sort_values(["年份", "省份"]).reset_index(drop=True)


def build_national_table() -> pd.DataFrame:
    rows = []
    for year, v in NATIONAL.items():
        rows.append({"年份": year, **v,
                     "第一产业占比": round(v["第一产业"] / v["GDP总量"], 4),
                     "第二产业占比": round(v["第二产业"] / v["GDP总量"], 4),
                     "第三产业占比": round(v["第三产业"] / v["GDP总量"], 4)})
    return pd.DataFrame(rows).sort_values("年份").reset_index(drop=True)


def main() -> None:
    long = build_long_table()
    wide = long.pivot(index="省份", columns="年份", values="GDP总量")
    nat = build_national_table()

    long.to_csv(DATA_DIR / "gdp_2015_2024.csv", index=False, encoding="utf-8-sig")
    wide.to_csv(DATA_DIR / "gdp_wide_total.csv", encoding="utf-8-sig")
    nat.to_csv(DATA_DIR / "gdp_national.csv", index=False, encoding="utf-8-sig")

    # 自检
    assert len(long) == len(YEARS) * len(PROVINCES) == 310, f"长表行数异常: {len(long)}"
